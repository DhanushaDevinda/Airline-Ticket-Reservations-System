package com.airlinereservation.model;

public class Flightstatus {

	private String route;
	private String flight;
	private String depday;
	private String ariday;
	private String depair;
	private String retair;
	private String date;
	private String search;
	
	public String getRoute() {
		return route;
	}
	public void setRoute(String route) {
		this.route = route;
	}
	public String getFlight() {
		return flight;
	}
	public void setFlight(String flight) {
		this.flight = flight;
	}
	public String getDepday() {
		return depday;
	}
	public void setDepday(String depday) {
		this.depday = depday;
	}
	public String getAriday() {
		return ariday;
	}
	public void setAriday(String ariday) {
		this.ariday = ariday;
	}
	public String getDepair() {
		return depair;
	}
	public void setDepair(String depair) {
		this.depair = depair;
	}
	public String getRetair() {
		return retair;
	}
	public void setRetair(String retair) {
		this.retair = retair;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getSearch() {
		return search;
	}
	public void setSearch(String search) {
		this.search = search;
	}
}
